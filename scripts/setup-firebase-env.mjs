/**
 * Interactive helper — writes .env from your Firebase web app config.
 * Run: npm run setup:firebase
 */
import { createInterface } from 'node:readline/promises';
import { writeFileSync, existsSync } from 'node:fs';
import { stdin as input, stdout as output } from 'node:process';

const PROJECT_ID = 'aptitudearcade';
const rl = createInterface({ input, output });

function clean(value) {
  return value.trim().replace(/^["']|["']$/g, '');
}

async function ask(label, fallback = '') {
  const suffix = fallback ? ` [${fallback}]` : '';
  const answer = clean(await rl.question(`${label}${suffix}: `));
  return answer || fallback;
}

async function main() {
  console.log('');
  console.log('Firebase setup for Aptitude Arcade');
  console.log('Open: https://console.firebase.google.com/project/aptitudearcade/settings/general');
  console.log('Scroll to "Your apps" → Web app → copy the config values.');
  console.log('');

  const apiKey = await ask('apiKey');
  const messagingSenderId = await ask('messagingSenderId');
  const appId = await ask('appId');
  const authDomain = await ask('authDomain', `${PROJECT_ID}.firebaseapp.com`);
  const storageBucket = await ask('storageBucket', `${PROJECT_ID}.firebasestorage.app`);
  const measurementId = await ask('measurementId (optional)', '');

  if (!apiKey || !messagingSenderId || !appId) {
    console.error('\napiKey, messagingSenderId, and appId are required.');
    process.exit(1);
  }

  const env = `# Firebase — project: ${PROJECT_ID}
# Generated by npm run setup:firebase
VITE_FIREBASE_API_KEY=${apiKey}
VITE_FIREBASE_AUTH_DOMAIN=${authDomain}
VITE_FIREBASE_PROJECT_ID=${PROJECT_ID}
VITE_FIREBASE_STORAGE_BUCKET=${storageBucket}
VITE_FIREBASE_MESSAGING_SENDER_ID=${messagingSenderId}
VITE_FIREBASE_APP_ID=${appId}
VITE_FIREBASE_MEASUREMENT_ID=${measurementId}
`;

  const envPath = '.env';
  if (existsSync(envPath)) {
    const overwrite = clean(await rl.question('.env already exists. Overwrite? (y/N): '));
    if (overwrite.toLowerCase() !== 'y') {
      console.log('Cancelled. No changes made.');
      process.exit(0);
    }
  }

  writeFileSync(envPath, env, 'utf8');

  console.log('');
  console.log('Wrote .env successfully.');
  console.log('');
  console.log('Next steps:');
  console.log('  1. Local:  npm run dev');
  console.log('  2. Vercel: Add the same VITE_FIREBASE_* vars in Project → Settings → Environment Variables');
  console.log('  3. Vercel: Redeploy the site');
  console.log('  4. Admin:  npm run seed:admin  (creates admin@aptitudearcade.com / arcade@123)');
  console.log('');
}

main()
  .catch((error) => {
    console.error(error);
    process.exit(1);
  })
  .finally(() => rl.close());
